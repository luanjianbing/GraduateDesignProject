#pragma once

#ifndef BLOCKQUEUE_H
#define BLOCKQUEUE_H

#include <iostream>
#include <condition_variable>
#include <mutex>

namespace Model {
	// 阻塞队列，用于日志输出模块
	template <class T>
	class BlockQueue {
	public:
		BlockQueue(int max_size = 1000) {
			if (max_size <= 0){
				std::cout << "Block Queue max_size Error" << std::endl;
				exit(-1);
			}

			m_max_size = max_size;
			m_array = new T[max_size];
			m_size = 0;
			m_front = -1;
			m_back = -1;
		}

		void clear()
		{
			m_mutex.lock();
			m_size = 0;
			m_front = -1;
			m_back = -1;
			m_mutex.unlock();
		}

		~BlockQueue() {
			m_mutex.lock();
			if (m_array != NULL)
				delete[] m_array;

			m_mutex.unlock();
		}

		//判断队列是否满了
		bool full()
		{
			m_mutex.lock();
			if (m_size >= m_max_size)
			{

				m_mutex.unlock();
				return true;
			}
			m_mutex.unlock();
			return false;
		}

		//判断队列是否为空
		bool empty()
		{
			m_mutex.lock();
			if (0 == m_size)
			{
				m_mutex.unlock();
				return true;
			}
			m_mutex.unlock();
			return false;
		}

		//返回队首元素
		bool front(T &value)
		{
			m_mutex.lock();
			if (0 == m_size)
			{
				m_mutex.unlock();
				return false;
			}
			value = m_array[m_front];
			m_mutex.unlock();
			return true;
		}

		//返回队尾元素
		bool back(T &value)
		{
			m_mutex.lock();
			if (0 == m_size)
			{
				m_mutex.unlock();
				return false;
			}
			value = m_array[m_back];
			m_mutex.unlock();
			return true;
		}

		int size()
		{
			int tmp = 0;

			m_mutex.lock();
			tmp = m_size;

			m_mutex.unlock();
			return tmp;
		}

		int max_size()
		{
			int tmp = 0;

			m_mutex.lock();
			tmp = m_max_size;

			m_mutex.unlock();
			return tmp;
		}

		//往队列添加元素，需要将所有使用队列的线程先唤醒
		//当有元素push进队列,相当于生产者生产了一个元素
		//若当前没有线程等待条件变量,则唤醒无意义
		bool push(const T &item)
		{

			m_mutex.lock();
			if (m_size >= m_max_size)
			{

				m_cond.notify_all();
				m_mutex.unlock();
				return false;
			}

			m_back = (m_back + 1) % m_max_size;
			m_array[m_back] = item;

			m_size++;

			m_cond.notify_all();
			m_mutex.unlock();
			return true;
		}

		//pop时,如果当前队列没有元素,将会等待条件变量
		bool pop(T &item)
		{

			//m_mutex.lock();
			std::unique_lock <std::mutex> lck(m_mutex);
			while (m_size <= 0)
			{
				m_cond.wait(lck);
				//if (!m_cond.wait(m_mutex.get()))
				//{
				//	m_mutex.unlock();
				//	return false;
				//}
			}

			m_front = (m_front + 1) % m_max_size;
			item = m_array[m_front];
			m_size--;
			//m_mutex.unlock();
			return true;
		}

	private:
		std::mutex m_mutex;
		std::condition_variable m_cond;

		T *m_array;
		int m_size;
		int m_max_size;
		int m_front;
		int m_back;
	};
}

#endif